package idea.tad;

public class Tad {

    public double xa;
    public double xb;
    public double ya;
    public double yb;

    public Tad(double xa, double xb, double ya, double yb){

        this.xa = xa;
        this.xb = xb;
        this.ya = ya;
        this.yb = yb;

    }

    public double getXa() {
        return xa;
    }

    public double getXb() {
        return xb;
    }

    public double getYa() {
        return ya;
    }

    public double getYb() {
        return yb;
    }


}
