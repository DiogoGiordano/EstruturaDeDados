package idea.tad;

public class Aluno {

    private String nome;
    private double nota;
    private int idade;

    public String getNome() {
        return nome;
    }
    public void setNome(String nome) {
        this.nome = nome;
    }
    private double getNota() {
        return nota;
    }
    private void setNota(double nota) {
        this.nota = nota;
    }
    public int getIdade() {
        return idade;
    }
    private void setIdade(int idade) {
        this.idade = idade;
    }

}
